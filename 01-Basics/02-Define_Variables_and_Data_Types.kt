fun main() {
    
    //DEFINE A VARIABLE 
    
    val x = 0 // val keyword - Use when you expect the variable value will not change.
    var y = 20 // var keyword - Use when you expect the variable value can change.
    
    
    //DATA TYPES
    
    val booleanVar: Boolean = true // Boolean can only be true or false
    val byteVar: Byte = 100 // Byte range: -128 to 127
    val shortVar: Short = 30000 // Short range: -32,768 to 32,767
    val intVar: Int = 1000000 // Int range: -2,147,483,648 to 2,147,483,647
    val longVar: Long = 9000000000L // Long range: -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
    val floatVar: Float = 3.14F // Float range: 1.4E-45 to 3.4028235E38
    val doubleVar: Double = 1.7976931348623157E308 // Double range: 4.9E-324 to 1.7976931348623157E308
    val charVar: Char = 'x' // Char represents a single character, e.g., 'x', 'y'
    val stringVar: String = "Hey there!" // String can hold a sequence of characters

}
